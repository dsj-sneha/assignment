// Write an if statement that checks if a variable fruit is "apple" or "banana". If it is, print "This is a fruit I like". Otherwise, print "I don't like this fruit".
void main() {
  String fruit = "banana";
  if (fruit == "apple" || fruit == "banana") {
    print("This is a fruit I like.");
  } else {
    print("This is the fruit I don't like.");
  }
}
